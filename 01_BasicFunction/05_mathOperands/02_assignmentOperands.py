# Addition Assignment (+=)
x = 5
x += 3  # Equivalent to x = x + 3
print("Addition Assignment (+=):", x)  # Output: 8

# Subtraction Assignment (-=)
y = 10
y -= 4  # Equivalent to y = y - 4
print("Subtraction Assignment (-=):", y)  # Output: 6

# Multiplication Assignment (*=)
z = 5
z *= 3  # Equivalent to z = z * 3
print("Multiplication Assignment (*=):", z)  # Output: 15

# Division Assignment (/=)
a = 10
a /= 2  # Equivalent to a = a / 2
print("Division Assignment (/=):", a)  # Output: 5.0

# Floor Division Assignment (//=)
b = 10
b //= 3  # Equivalent to b = b // 3
print("Floor Division Assignment (//=):", b)  # Output: 3

# Modulus Assignment (%=)
c = 10
c %= 3  # Equivalent to c = c % 3
print("Modulus Assignment (%=):", c)  # Output: 1

# Exponentiation Assignment (**=)
d = 2
d **= 3  # Equivalent to d = d ** 3
print("Exponentiation Assignment (**=):", d)  # Output: 8

# Bitwise AND Assignment (&=)
e = 6  # 0110 in binary
e &= 3  # 0011 in binary (bitwise AND)
print("Bitwise AND Assignment (&=):", e)  # Output: 2 (0010 in binary)

# Bitwise OR Assignment (|=)
f = 6  # 0110 in binary
f |= 3  # 0011 in binary (bitwise OR)
print("Bitwise OR Assignment (|=):", f)  # Output: 7 (0111 in binary)

# Bitwise XOR Assignment (^=)
g = 6  # 0110 in binary
g ^= 3  # 0011 in binary (bitwise XOR)
print("Bitwise XOR Assignment (^=):", g)  # Output: 5 (0101 in binary)

# Bitwise Left Shift Assignment (<<=)
h = 5  # 0101 in binary
h <<= 2  # Left shift by 2 positions
print("Bitwise Left Shift Assignment (<<=):", h)  # Output: 20 (10100 in binary)

# Bitwise Right Shift Assignment (>>=)
i = 20  # 10100 in binary
i >>= 2  # Right shift by 2 positions
print("Bitwise Right Shift Assignment (>>=):", i)  # Output: 5 (0101 in binary)
