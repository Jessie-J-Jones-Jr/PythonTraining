# Addition (+): Adds two operands
result_add = 5 + 3  # result is 8
print("Addition: ", result_add)

# Subtraction (-): Subtracts the right operand from the left operand
result_sub = 5 - 3  # result is 2
print("Subtraction: ", result_sub)

# Multiplication (*): Multiplies two operands
result_mul = 5 * 3  # result is 15
print("Multiplication: ", result_mul)

# Division (/): Divides the left operand by the right operand (returns a float)
result_div = 5 / 3  # result is 1.6667
print("Division: ", result_div)

# Floor Division (//): Divides the left operand by the right operand and returns the largest integer less than or equal to the result
result_floor_div = 5 // 3  # result is 1
print("Floor Division: ", result_floor_div)

# Modulus (%): Returns the remainder when the left operand is divided by the right operand
result_mod = 5 % 3  # result is 2
print("Modulus: ", result_mod)

# Exponentiation (**): Raises the left operand to the power of the right operand
result_exp = 5 ** 3  # result is 125
print("Exponentiation: ", result_exp)

# Unary Plus (+): Returns the value of the operand (indicates a positive number)
result_unary_plus = +5  # result is 5
print("Unary Plus: ", result_unary_plus)

# Unary Minus (-): Negates the value of the operand
result_unary_minus = -5  # result is -5
print("Unary Minus: ", result_unary_minus)

