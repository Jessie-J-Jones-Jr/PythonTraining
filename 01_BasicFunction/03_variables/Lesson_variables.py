#different types of variables within Python

# Integer
age = 30
print(type(age))  # Output: <class 'int'>
# Integer: Whole numbers, positive or negative, without a decimal point.

# Float
height = 5.9
print(type(height))  # Output: <class 'float'>
# Float: Numbers that have a decimal point or are in scientific notation.

# String
name = "Alice"
print(type(name))  # Output: <class 'str'>
# String: A sequence of characters enclosed in quotes (single or double).

# Boolean
is_student = True
print(type(is_student))  # Output: <class 'bool'>
# Boolean: A type that can hold one of two values: True or False.


# List
fruits = ["apple", "banana", "cherry"]
print(type(fruits))  # Output: <class 'list'>
# List: An ordered, mutable collection of items, which can be of different data types.

# Tuple
coordinates = (10, 20)
print(type(coordinates))  # Output: <class 'tuple'>
# Tuple: An ordered, immutable collection of items, which can be of different data types.

# Dictionary
person = {"name": "Alice", "age": 25}
print(type(person))  # Output: <class 'dict'>
# Dictionary: An unordered collection of key-value pairs, where each key is unique.

# Set
unique_numbers = {1, 2, 3}
print(type(unique_numbers))  # Output: <class 'set'>
# Set: An unordered collection of unique items (no duplicates allowed).
























